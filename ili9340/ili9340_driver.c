void ili9340_set_addr_window(struct ili9340_t *tft, uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
	ili9340_write_cmd(tft, 0x2A);
	ili9340_write_data(tft, x0 >> 8);
	ili9340_write_data(tft, x0 & 0xFF);
	ili9340_write_data(tft, x1 >> 8);
	ili9340_write_data(tft, x1 & 0xFF);

	ili9340_write_cmd(tft, 0x2B);
	ili9340_write_data(tft, y0 >> 8);
	ili9340_write_data(tft, y0 & 0xFF);
	ili9340_write_data(tft, y1 >> 8);
	ili9340_write_data(tft, y1 & 0xFF);

	ili9340_write_cmd(tft, 0x2C);
}

void ili9340_push_color(struct ili9340_t *tft, uint16_t color) {
	gpio_pin_high(tft->handle, tft->pin_dc);
	gpio_pin_low(tft->handle, tft->pin_cs);

	ili9340_spi_write(tft, color >> 8);
	ili9340_spi_write(tft, color & 0xFF);

	gpio_pin_high(tft->handle, tft->pin_cs);
}

void ili9340_spi_write(struct ili9340_t *tft, uint8_t data) {
	for (int8_t i = 7; i >= 0; i--) {
		if (data & (1 << i)) {
			gpio_pin_high(tft->handle, tft->pin_mosi);
		} else {
			gpio_pin_low(tft->handle, tft->pin_mosi);
		}

		gpio_pin_high(tft->handle, tft->pin_sck);
		gpio_pin_low(tft->handle, tft->pin_sck);
	}
}

void ili9340_write_cmd(struct ili9340_t *tft, uint8_t cmd) {
	gpio_pin_low(tft->handle, tft->pin_dc);
	gpio_pin_low(tft->handle, tft->pin_sck);
	gpio_pin_low(tft->handle, tft->pin_cs);

	ili9340_spi_write(tft, cmd);

	gpio_pin_high(tft->handle, tft->pin_cs);
}

void ili9340_write_data(struct ili9340_t *tft, uint8_t data) {
	gpio_pin_high(tft->handle, tft->pin_dc);
	gpio_pin_low(tft->handle, tft->pin_sck);
	gpio_pin_low(tft->handle, tft->pin_cs);

	ili9340_spi_write(tft, data);

	gpio_pin_high(tft->handle, tft->pin_cs);
}
