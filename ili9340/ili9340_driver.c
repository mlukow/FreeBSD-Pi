#define I9340_WIDTH 320
#define I9340_HEIGHT 240

struct ili9340_t {
	gpio_handle_t handle;

	gpio_pin_t pin_cs;
	gpio_pin_t pin_rst;
	gpio_pin_t pin_dc;
	gpio_pin_t pin_sck;
	gpio_pin_t pin_mosi;
	gpio_pin_t pin_miso;

	uint16_t posx;
	uint16_t posy;
};

void ili9340_push_color(struct ili9340_t *tft, uint16_t color);
void ili9340_set_addr_window(struct ili9340_t *tft, uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1);
void ili9340_spi_write(struct ili9340_t *tft, uint8_t data);
void ili9340_write_cmd(struct ili9340_t *tft, uint8_t cmd);
void ili9340_write_data(struct ili9340_t *tft, uint8_t data);

uint16_t get_color(uint8_t r, uint8_t g, uint8_t b) {
	return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

void ili9340_clear(struct ili9340_t *tft) {
	uint32_t color = get_color(0x00, 0x00, 0x00);
	for (uint16_t y = 0; y < I9340_HEIGHT; y++) {
		ili9340_set_addr_window(tft, 0, y, I9340_WIDTH, y);
		for (uint16_t x = 0; x < I9340_WIDTH; x++) {
			ili9340_push_color(tft, color);
		}
	}
}

void ili9340_draw_pixel(
		struct ili9340_t *tft,
		uint16_t x,
		uint16_t y,
		uint32_t color) {
	ili9340_set_addr_window(tft, x, y, x + 1, y + 1);
	ili9340_push_color(tft, color);
}

