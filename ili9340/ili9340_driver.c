void ili9340_va_printf(struct ili9340_t *tft, const char *fmt, va_list args) {
	char buf[BUFSIZ];

	int ret = vsnprintf(buf, BUFSIZ, fmt, args);
	uint32_t fg = get_color(0xFF, 0xFF, 0xFF);
	uint32_t bg = get_color(0x00, 0x00, 0x00);

	for (int i = 0; i < ret; i++) {
		if (buf[i] == '\n') {
			tft->posx = 0;
			tft->posy += 8;
		} else {
			ili9340_putc(tft, buf[i], tft->posx, tft->posy, fg, bg);
			tft->posx += 5;
		}
	}
}

void ili9340_printf(struct ili9340_t *tft, const char *fmt, ...) {
	va_list ap;
	va_start(ap, fmt);

	ili9340_va_printf(tft, fmt, ap);

	va_end(ap);
}

void ili9340_putc(
		struct ili9340_t *tft,
		char c,
		uint16_t x,
		uint16_t y,
		uint32_t fg,
		uint32_t bg) {
	char ch;
	for (uint8_t i = 0; i < 6; i++) {
		for (uint8_t j = 0; j < 8; j++) {
			if (i == 5) {
				ch = 0;
			} else {
				ch = font_5x7[c * 5 + i];
			}

			ili9340_draw_pixel(tft, x + i, y + j, (ch & (1 << j)) ? fg : bg);
		}
	}
}

void ili9340_puts(
		struct ili9340_t *tft,
		const char *s,
		uint16_t x,
		uint16_t y,
		uint32_t fg,
		uint32_t bg) {
	uint16_t current = 0;
	while (*s) {
		if (x + current >= I9340_WIDTH) {
			break;
		}

		ili9340_putc(tft, *s, x + current, y, fg, bg);
		s++;
		x += 6;
	}
}

